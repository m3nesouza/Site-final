function carregarRedacoes() {
    const grid = document.getElementById('redacoesGrid');
    
    redacoes.forEach(redacao => {
        const card = document.createElement('div');
        card.className = 'redacao-card fade-in';
        card.innerHTML = `
            <div class="imagem-redacao">
                <div class="placeholder-redacao">
                    <i class="fas fa-file-alt"></i>
                    <span>Redação ${redacao.id}</span>
                </div>
            </div>
            <h3>${redacao.titulo}</h3>
            <p>${redacao.descricao}</p>
            <div class="botoes-redacao">
                <button class="btn btn-ver" onclick="verRedacao(${redacao.id})">
                    <i class="fas fa-eye"></i> Ler Redação
                </button>
            </div>
        `;
        grid.appendChild(card);
    });
}

function verRedacao(id) {
    const redacao = redacoes.find(r => r.id === id);
    redacaoAtual = redacao;
    
    const modal = document.createElement('div');
    modal.className = 'modal-redacao';
    modal.innerHTML = `
        <div class="modal-conteudo">
            <div class="modal-cabecalho">
                <h2>${redacao.titulo}</h2>
                <button class="btn-fechar" onclick="fecharModal()">
                    <i class="fas fa-times"></i>
                </button>
            </div>
            <div class="modal-corpo">
                ${redacao.textoCompleto}
            </div>
            <div class="modal-rodape">
                <button class="btn btn-voltar" onclick="fecharModal()">
                    <i class="fas fa-arrow-left"></i> Voltar para a lista
                </button>
            </div>
        </div>
    `;
    
    document.body.appendChild(modal);
    document.body.style.overflow = 'hidden';
}

function fecharModal() {
    const modal = document.querySelector('.modal-redacao');
    if (modal) {
        modal.remove();
        document.body.style.overflow = 'auto';
    }
}

document.getElementById('search').addEventListener('input', function(e) {
    const termo = e.target.value.toLowerCase();
    const cards = document.querySelectorAll('.redacao-card');
    
    cards.forEach(card => {
        const titulo = card.querySelector('h3').textContent.toLowerCase();
        const descricao = card.querySelector('p').textContent.toLowerCase();
        
        if (titulo.includes(termo) || descricao.includes(termo)) {
            card.style.display = 'block';
        } else {
            card.style.display = 'none';
        }
    });
});

function scrollToSection(id) {
    const element = document.getElementById(id);
    if (element) {
        element.scrollIntoView({
            behavior: 'smooth'
        });
    }
}

window.addEventListener('scroll', () => {
    const sections = document.querySelectorAll('section');
    const navLinks = document.querySelectorAll('.nav-menu a');
    
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop - 100;
        if (window.scrollY >= sectionTop) {
            current = section.getAttribute('id');
        }
    });

    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') {
        fecharModal();
    }
});

document.addEventListener('DOMContentLoaded', () => {
    carregarRedacoes();
    
    const cards = document.querySelectorAll('.fade-in');
    cards.forEach((card, index) => {
        card.style.animationDelay = `${index * 0.1}s`;
    });
});